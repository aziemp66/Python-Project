x = 10
y = 5

#operasi penjumlahan ( + )
penjumlahan = x + y
print(x,'+',y,'=',penjumlahan)

#operasi pengurangan ( - )
pengurangan = x - y
print(x,'-',y,'=',pengurangan)

#operasi perkalian  ( * )
perkalian = x * y
print(x,'*',y,'=',perkalian)

#operasi pembagian ( / )
pembagian = x / y
print(x,'/',y,'=',pembagian)

#operasi pemangkatan ( ** )
pemangkatan = x ** y
print(x,'**',y,'=',pemangkatan)

#operasi pembagian bulat ( // )
pembagian_bulat = x // y
print(x,'//',y,'=',pembagian_bulat)

#operasi modulus ( % )
modulus = x % y
print(x,'%',y,'=',modulus)