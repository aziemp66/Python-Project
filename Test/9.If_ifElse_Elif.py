
nama = "Adi"
# 1. if Statement

if nama=="Adi":
	print("hai Adi")

# 2. If Else Statement

if nama=="Adi":
	print("hai Adi")
else:
	print("anda bukan Adi")


# 3. Elif Statement
if nama=="Adi":
    print("hai Adi")
elif nama=="Juni":
    print("hai Juni")
elif nama=="Hafiz":
    print("hai Hafiz")
else:
    print("anda bukan Adi,Hafiz atau Juni")