print("If Else adalah sebuah pengkondisian dalam python yang dimana apabila if bernilai true maka akan mengeksekusikan if, apabila salah maka akan mengeksekusi else")

number = int(input("Masukkan sebuah angka : "))

if(number > 10):#apabila benar if akan dieksekusi, apabila salah else akan dieksekusi
    print("Number lebih dari 10")
else:
    print("Number kurang dari atau sama dengan 10")